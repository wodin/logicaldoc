package com.logicaldoc.gui.common.client.beans;

/**
 * A simple tag representation
 * 
 * @author Marco Meschieri - Logical Objects
 * @since 6.0
 */
public class GUITag {
	private String tag;

	private int count;

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

}